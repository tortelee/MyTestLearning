package com.fortest;

public class Calcultor {

    public Calcultor() {
    }

    public int add(int a, int b){
        return a+b;
    }
    public int minus(int a,int b){
        return a-b;
    }
    public int multiply(int a,int b){
        return a*b;
    }
    public int deply(int a,int b){
        return a/b;
    }
}
